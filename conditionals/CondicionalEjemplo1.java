package condicionalejemplo1;
public class CondicionalEjemplo1 {
    public static void main(String[] args) {
    int x =10;
    int y =12;
    if(x+y > 20){
        System.out.println("x + y es mayor que 20");
    }
    else{
        System.out.println("x + y es menor que 20");
    }
    }
}
